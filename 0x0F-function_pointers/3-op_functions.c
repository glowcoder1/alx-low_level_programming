#include "3-calc.h"

/**
 * op_add - adds a and b
 * @a: first num
 * @b: second param
 * Return: result of op_add
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts a and b
 * @a: first num
 * @b: second param
 * Return: result of op_sum
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a and b
 * @a: first num
 * @b: second param
 * Return: result of op_mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a by b
 * @a: first num
 * @b: second param
 * Return: result of op_div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division a and b
 * @a: first num
 * @b: second param
 * Return: result of op_div
 */

int op_mod(int a, int b)
{
	return (a % b);
}
